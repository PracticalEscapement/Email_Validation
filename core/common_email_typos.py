def get_typo_correction(domain):
    """
    Get the corrected domain if a typo is found
    
    Args:
        domain (str): The domain to check
        
    Returns:
        str: Corrected domain or original domain if no typo found
    """
    domain_lower = domain.lower()
    return COMMON_DOMAIN_TYPOS.get(domain_lower, domain)